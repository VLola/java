package com.example.Project_119.models;

public class Trip {

    protected double Fuel;
    protected double Price;

    public double getFuel() {
        return Fuel;
    }

    public double getPrice() {
        return Price;
    }

    public void setFuel(double fuel) {
        Fuel = fuel;
    }

    public void setPrice(double price) {
        Price = price;
    }
}
